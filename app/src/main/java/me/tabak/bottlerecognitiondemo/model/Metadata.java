package me.tabak.bottlerecognitiondemo.model;

public class Metadata {
    private int wine_id;
    private String image_url;
    private String wine_com_url;
    private String vintage;
    private String winery_name;

    public int getWineId() {
        return wine_id;
    }

    public String getImageUrl() {
        return image_url;
    }

    public String getWineComUrl() {
        return wine_com_url;
    }

    public String getVintage() {
        return vintage;
    }

    public String getWineryName() {
        return winery_name;
    }
}
